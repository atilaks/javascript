let num_base = 10;

// +=: incrementa en x el valor de una variable
let x1 = 5;
let mod_num1 = num_base += x1;
alert(mod_num1);

// -=: decrementa en x el valor de una variable
let x2 = 5;
let mod_num2 = num_base -= x2;
alert(mod_num2);

// *=: multiplica en x el valor de una variable
let x3 = 5;
let mod_num3 = num_base *= x3;
alert(mod_num3);

// /=: divide en x el valor de una variable
let x4 = 5;
let mod_num4 = num_base /= x4;
alert(mod_num4);

// ++: incrementa en 1 el valor de una variable
let mod_num5 = ++ num_base;
alert(mod_num5);

// --: decrementa en 1 el valor de una variable
let mod_num6 = -- num_base;
alert(mod_num6);
